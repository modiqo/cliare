fn benchmark_agent_skill() -> &'static str {
    r#"---
name: cliare-benchmark-review
description: Use when reviewing a CLIARE benchmark artifact directory, comparing CLI targets, explaining calibration metrics, or drilling into per-target measurement artifacts.
---

# CLIARE Benchmark Review

## What This Is

This is an artifact-local guide for an agent reviewing one CLIARE benchmark directory. A benchmark directory compares multiple CLI measurements, records corpus-level calibration metrics, and links each target back to its own measurement artifact directory.

Use it when the task is to explain a corpus run, compare targets, identify calibration drift, or decide which target artifact needs deeper review. Benchmark-level data shows comparative posture; target-level directories contain the evidence needed to recommend CLI changes.

Start at `benchmark.json`, then drill into target artifact directories for evidence.

## Workflow

1. Summarize corpus health.

```sh
jq '{totals,calibration:{score_mean:.calibration.score_mean,score_min:.calibration.score_min,score_max:.calibration.score_max,expected_band_pass_rate:.calibration.expected_band_pass_rate,traversal_completion_rate:.calibration.traversal_completion_rate,budget_exhaustion_rate:.calibration.budget_exhaustion_rate}}' benchmark.json
```

2. Rank targets by score and operational pressure.

```sh
jq '.targets[] | {id,status,score,expected_score,duration_ms,probes_completed,traversal_complete,budget_exhausted,observed_max_depth,max_depth,issues,artifact_dir}' benchmark.json
```

3. Separate benchmark failures from CLI findings. A benchmark target can fail because it missed an expected band, exceeded a duration budget, skipped a required binary, or produced CLIARE findings. Use the target `issues` field for benchmark-level failures and the target directory for CLI-level findings.

4. For a target that needs review, enter its `artifact_dir`, read `README.md`, then inspect `scorecard.json`, `issues.json`, `command-index.json`, `shape.json`, and `evidence.jsonl`.

```sh
target_dir="$(jq -r '.targets[] | select(.id=="cliare") | .artifact_dir' benchmark.json)"
cd "$target_dir"
jq '{score:.score.total,coverage:.coverage}' scorecard.json
```

5. Refresh persona reports inside the target directory when a reviewer needs a focused packet.

```sh
cliare report maintainer --out . --write
```

## Output Style

Report benchmark results as corpus-level trends first, then target-level evidence. Do not infer a target defect from a corpus metric alone. Use the target artifact directory before recommending a CLI change.
"#
}

